
public class Logic {
	
	public static float CalculateSquareArea(float side) {
		return side * side;
	}
	
	public static float CalculateRectangleArea(float base, float high) {
		return base * high;
	}
	
	public static float CalculateTriangleArea(float base, float high) {
		
		return base * high / 2.0f;
	}

}
